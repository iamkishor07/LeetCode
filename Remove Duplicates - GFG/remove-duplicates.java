class Solution {
    String removeDups(String S) {
        // code here
        Set<Character>s=new LinkedHashSet<>();
        for(Character i:S.toCharArray()){
            s.add(i);
        }
        String ans="";
        for(char a:s){
            ans+=a;
        }
        return ans;
    }
}